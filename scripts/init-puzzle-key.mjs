#!/usr/bin/env node
/** Generate a new obfuscated AES key in js/puzzle-file-key.js (one-time or rotation). */

import { randomBytes } from "node:crypto";
import { writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { obfuscatePuzzleFileKey } from "../js/puzzle-file-key.js";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const outPath = resolve(root, "js/puzzle-file-key.js");

const rawKey = randomBytes(32);
const obfuscated = obfuscatePuzzleFileKey(rawKey);

const lines = obfuscated.map((b) => String(b)).join(", ");
const source = `// Generated by npm run init:puzzle-key

const PUZZLE_KEY_OBFUSCATION_SALT = "wordhunter-puzzle-v1";

export const PUZZLE_FILE_KEY_OBFUSCATED = [
  ${lines},
];

export function deobfuscatePuzzleFileKey() {
  const salt = PUZZLE_KEY_OBFUSCATION_SALT;
  const out = new Uint8Array(PUZZLE_FILE_KEY_OBFUSCATED.length);
  for (let i = 0; i < PUZZLE_FILE_KEY_OBFUSCATED.length; i++) {
    out[i] = PUZZLE_FILE_KEY_OBFUSCATED[i] ^ salt.charCodeAt(i % salt.length);
  }
  return out;
}

export function obfuscatePuzzleFileKey(rawKey) {
  const salt = PUZZLE_KEY_OBFUSCATION_SALT;
  const out = new Array(rawKey.length);
  for (let i = 0; i < rawKey.length; i++) {
    out[i] = rawKey[i] ^ salt.charCodeAt(i % salt.length);
  }
  return out;
}
`;

writeFileSync(outPath, source, "utf8");
console.log("Wrote", outPath);
console.log(
  "Re-run npm run encrypt:puzzles to rebuild text/puzzles.enc with the new key."
);
